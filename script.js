// Theme Switching Functionality
class ThemeManager {
    constructor() {
        this.themeToggle = document.getElementById('themeToggle');
        this.lightIcon = document.getElementById('lightIcon');
        this.darkIcon = document.getElementById('darkIcon');
        this.html = document.documentElement;
        
        this.init();
    }
    
    init() {
        // Check for saved theme preference or default to system preference
        const savedTheme = localStorage.getItem('theme');
        const systemPrefersDark = window.matchMedia('(prefers-color-scheme: dark)').matches;
        
        if (savedTheme) {
            this.setTheme(savedTheme);
        } else {
            this.setTheme(systemPrefersDark ? 'dark' : 'light');
        }
        
        // Add event listener for theme toggle
        this.themeToggle.addEventListener('click', () => {
            const currentTheme = this.html.getAttribute('data-theme');
            const newTheme = currentTheme === 'light' ? 'dark' : 'light';
            this.setTheme(newTheme);
        });
        
        // Listen for system theme changes
        window.matchMedia('(prefers-color-scheme: dark)').addEventListener('change', (e) => {
            if (!localStorage.getItem('theme')) {
                this.setTheme(e.matches ? 'dark' : 'light');
            }
        });
    }
    
    setTheme(theme) {
        this.html.setAttribute('data-theme', theme);
        localStorage.setItem('theme', theme);
        
        // Update toggle button appearance
        if (theme === 'dark') {
            this.lightIcon.style.display = 'none';
            this.darkIcon.style.display = 'inline-block';
            this.themeToggle.setAttribute('aria-label', 'Switch to light mode');
        } else {
            this.lightIcon.style.display = 'inline-block';
            this.darkIcon.style.display = 'none';
            this.themeToggle.setAttribute('aria-label', 'Switch to dark mode');
        }
        
        // Add smooth transition class
        this.html.classList.add('theme-transitioning');
        setTimeout(() => {
            this.html.classList.remove('theme-transitioning');
        }, 300);
    }
}

